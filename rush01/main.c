void    ft_display(int **grille);

int		main(void)
{
	int i;
	int j;
	int **grille;

	while (i < 9)
	{
		while (j < 9)
		{
			grille[i][j] = 0;
			j++;
		}
		i++;
	}
	ft_display(grille);
	return(0);
}
