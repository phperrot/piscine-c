int		ft_str_is_lowercase(char *str)
{
	int i;
	int is_lower;

	i = 0;
	is_lower = 1;
	while (str[i] != '\0')
	{
		if ((str[i] < 97) | (str[i] > 122))
		{
			is_lower = 0;
		}
		i++;
	}
	return (is_lower);
}
