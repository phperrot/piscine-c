int		ft_str_is_alpha(char *str)
{
	int i;
	int is_numeric;

	i = 0;
	is_numeric = 1;
	while (str[i] != '\0')
	{
		if (((str[i] < 65) | (str[i] > 90) && (str[i] < 97) | (str[i] > 122)))
		{
			is_numeric = 0;
		}
		i++;
	}
	return (is_numeric);
}
