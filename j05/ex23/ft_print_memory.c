int	ft_putchar(char c);

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

void *ft_print_memory(void *addr, unsigned int size)
{
	ft_putstr(addr);
	return(addr);
}
