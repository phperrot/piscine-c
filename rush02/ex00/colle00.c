void		colle00(int x, int y)
{
	int		i;
	int		j;

	i = 1;
	while (i <= y)
	{
		j = 1;
		while (j <= x)
		{
			if ((i == 1 || i == y) && (j == 1 || j == x))
				ft_putchar('o');
			else if ((i > 1 && i < y) && (j == 1 || j == x))
				ft_putchar('|');
			else if ((j > 1 && j < x) && (i == 1 || i == y))
				ft_putchar('-');
			else
				ft_putchar(' ');
			j++;
		}
		if (y > 0)
			ft_putchar('\n');
		i++;
	}
}
